    private void AddCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddCourseActionPerformed
        // TODO add your handling code here:
        String coursTitle  =  CourseNameField.getText();
        String creditHours =  CreditHoursField.getText();
        String marks       =  MarksField.getText();
        String grade       =  gradeCalculator(Double.parseDouble(marks)); 
        double gradePoint  =  gradePointCalculator(grade);      
        double product     = Integer.parseInt(creditHours)*gradePoint;
        
        Connection con = null ;
        PreparedStatement inputs = null ;
        ResultSet rs = null ;     
        
        try{
           Class.forName(DataBaseConnection.DriverPath);
           con = DriverManager.getConnection(DataBaseConnection.DatabaseFile);
           String query = "INSERT INTO GPACalculator (CourseTitle, CreditHours, Marks, Grade, GradePoint, Product) VALUES (?,?,?,?,?,?)";
           inputs = con.prepareStatement(query);
           inputs.setString(1, coursTitle);
           inputs.setString(2, creditHours);
           inputs.setString(3, marks);
           inputs.setString(4, grade);
           inputs.setDouble(5, gradePoint);
           inputs.setDouble(6, product);
           
           inputs.executeUpdate();

           CourseNameField.setText("");
           CreditHoursField.setText("");
           MarksField.setText("");
           
           LoadGPACalculatorRecordTable();
        }
        
        catch(Exception e ){
               JOptionPane.showMessageDialog(null, e);
        }  
    }//GEN-LAST:event_AddCourseActionPerformed
    
    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        // TODO add your handling code here:
        String ID = CourseNameField.getText();
        if(!CourseNameField.getText().equals(""))
        {
            Connection con = null;
            PreparedStatement ps = null;
            ResultSet rs = null;
            try{
                Class.forName(DataBaseConnection.DriverPath);
                con = DriverManager.getConnection(DataBaseConnection.DatabaseFile);

                String query = "Delete from GPACalculator where CourseTitle = ?";
                ps = con.prepareStatement(query);
                ps.setString(1,ID);
                int result = ps.executeUpdate();
                if(result > 0){
                    JOptionPane.showMessageDialog(null, "Course Sucessfully Removed.");
                    DefaultTableModel model = (DefaultTableModel)GPAResultsRecordTable.getModel();
                    model.setRowCount(0);
                    
                    CourseNameField.setText("");
                    CreditHoursField.setText("");
                    MarksField.setText("");              
                    LoadGPACalculatorRecordTable();
                }
                
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Course Must Be Selected First From Table.");
        }
    }//GEN-LAST:event_DeleteActionPerformed

