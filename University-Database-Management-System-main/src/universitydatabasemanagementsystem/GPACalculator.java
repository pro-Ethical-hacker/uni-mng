    /**
     * Calculates the grade for a given set of marks.
     *
     * This method takes a numeric value representing marks and determines
     * the corresponding letter grade based on a predefined grading scale.
     *
     * @param marks The numeric marks obtained.
     * @return The corresponding letter grade.
     */
    public String gradeCalculator(double marks)
    {
        String G = "";
        if(marks >= 85 && marks <= 100)
        {
            G = "A";
        }
        else if(marks >= 80)
        {
            G = "A-";
        }
        else if(marks >= 75)
        {
            G = "B+";
        }
        else if(marks >= 71)
        {
            G = "B";
        }
        else if(marks >= 68)
        {
            G = "B-";
        }
        else if(marks >= 64)
        {
            G = "C+";
        }
        else if(marks >= 60)
        {
            G = "C";
        }
        else if(marks >= 57)
        {
            G = "C-";
        }
        else if(marks >= 53)
        {
            G = "D+";
        }
        else if(marks >= 50)
        {
            G = "D";
        }
        else if(marks < 50 && marks > 0)
        {
            G = "F";
        }
  
        return G ;
    }
    
    /**
     * Calculates the grade points for a given letter grade.
     *
     * This method takes a letter grade and returns the associated grade points
     * based on a predefined grade-point scale.
     *
     * @param grade The letter grade.
     * @return The grade points associated with the grade.
     */
    public double gradePointCalculator(String grade)
    {
        double GP = 0.0;
        if(grade == "A")
        {
            GP = 4.0;
        }
        else if(grade == "A-")
        {
            GP = 3.67;
        }
        
        else if(grade == "B+")
        {
            GP = 3.33;
        }
        
        else if(grade == "B")
        {
            GP = 3.0;
        }
        
        else if(grade == "B-")
        {
            GP = 2.67;
        }
        
        else if(grade == "C+")
        {
            GP = 2.33;
        }
        
        else if(grade == "C")
        {
            GP = 2.0;
        }
        
        else if(grade == "C-")
        {
            GP = 1.87;
        }
        
        else if(grade == "D+")
        {
            GP = 1.33;
        }
        
        else if(grade == "D")
        {
            GP = 1.0;
        }
        
        else if(grade == "F")
        {
            GP = 0;
        }
        
        
        return GP ;
    }
    
