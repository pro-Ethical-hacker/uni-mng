    private void CalculateCGPAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalculateCGPAActionPerformed
        // TODO add your handling code here:
        DecimalFormat df2 = new DecimalFormat("#.##");
        
        double Sgpa = Double.parseDouble(SGPAField.getText());
        int  ScreditHours = Integer.parseInt(SCreditHoursField.getText());
        double Pcgpa = Double.parseDouble(PCGPAField.getText());
        int  PcreditHours = Integer.parseInt(PCreditHoursField.getText());
        
        double cgpa;
        int totalCreditHours;
        
        totalCreditHours = ScreditHours + PcreditHours;
        cgpa    = (Sgpa*ScreditHours + Pcgpa*PcreditHours)/totalCreditHours;
        
        CGPA.setText(df2.format(cgpa));
        TotalCreditHours.setText(Integer.toString(totalCreditHours));
        
    }//GEN-LAST:event_CalculateCGPAActionPerformed
    
    /**
     * Calculates the GPA for the current semester based on course records.
     *
     * This method calculates the GPA for the current semester by summing the
     * grade points obtained in each course and dividing it by the total credit
     * hours.
     *
     * @param evt The ActionEvent that triggered the calculation.
     */
    public void CalculateGPAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalculateGPAActionPerformed
        // TODO add your handling code here:
        
        DecimalFormat df2 = new DecimalFormat("#.##");
        
        int CreditHours = 0;
        double Product  = 0.0;
        double gpa = 0.0;
        
        Connection con = null;
        PreparedStatement ps = null;
        ResultSet rs = null;
        try{
            Class.forName(DataBaseConnection.DriverPath);
            con = DriverManager.getConnection(DataBaseConnection.DatabaseFile);
            String query = "select * from GPACalculator";
            ps = con.prepareStatement(query);
            rs = ps.executeQuery();
            while(rs.next()){
                CreditHours += rs.getInt("CreditHours");
                Product     += rs.getDouble("Product");            
            }
            gpa = Product/CreditHours;
            GPA.setText(df2.format(gpa));
            SemTotalCreditHours.setText(Integer.toString(CreditHours));
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_CalculateGPAActionPerformed
    
   
