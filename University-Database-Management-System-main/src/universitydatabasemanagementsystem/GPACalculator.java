    /**
     * Loads the GPACalculatorRecordTable with records from the database.
     *
     * This method retrieves course records from the database and populates
     * the table with the data.
     */
    public void LoadGPACalculatorRecordTable(){
        Connection con = null;
        PreparedStatement ps = null;
        ResultSet rs = null;
        try{
            Class.forName(DataBaseConnection.DriverPath);
            con = DriverManager.getConnection(DataBaseConnection.DatabaseFile);
            String query = "select * from GPACalculator";
            ps = con.prepareStatement(query);
            rs = ps.executeQuery();
            DefaultTableModel model = (DefaultTableModel)GPAResultsRecordTable.getModel();
            while(rs.next()){
                Object[] row = new Object[6];
                
                row[0] = rs.getString("CourseTitle");
                row[1] = rs.getString("CreditHours");
                row[2] = rs.getString("Marks");
                row[3] = rs.getString("Grade");
                row[4] = rs.getString("GradePoint");
                row[5] = rs.getString("Product");
                
                model.addRow(row); 
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
