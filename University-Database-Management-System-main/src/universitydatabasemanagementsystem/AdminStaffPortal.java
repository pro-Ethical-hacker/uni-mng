    /**
     * Action handler for the "Delete" button.
     * Deletes the selected administration staff record from the database.
     * @param evt The ActionEvent triggered by the button click.
     */
    public void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        // TODO add your handling code here:
        String ID           = IDField.getText();
        
        if(!IDField.getText().equals(""))
        {
            Connection con = null;
            PreparedStatement ps = null;
            
            try{
                Class.forName(DataBaseConnection.DriverPath);
                con = DriverManager.getConnection(DataBaseConnection.DatabaseFile);
                
                String query = "Delete from AdminStaff where ID =?";
                ps = con.prepareStatement(query);
                
                ps.setString(1, ID);
                
                int result = ps.executeUpdate();
                
                if(result > 0)
                {
                    JOptionPane.showMessageDialog(null, "Administration Staff Records Delete Sucessfully.");
                    DefaultTableModel model = (DefaultTableModel)StaffRecordTable.getModel();
                    model.setRowCount(0);
                    
                    IDField.setText("");
                    NameField.setText("");
                    DesignationField.setText("");
                    PhoneNoField.setText("");
                    
                    LoadAdminStaffRecordTable(); 
                }
                else{
                     JOptionPane.showMessageDialog(null, "Administration Staff Records Not Deleted.");
                }
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Administration Staff Record Must be Selected First From the Table.");       
        }
    }//GEN-LAST:event_DeleteButtonActionPerformed

