    private void IDSearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IDSearchButtonActionPerformed
        // TODO add your handling code here:
        if(!IDSearchField.getText().equals("")){
            
            String ID = IDSearchField.getText();
            Connection con = null;
            PreparedStatement ps = null;
            ResultSet rs = null;
            
            try{
                Class.forName(DataBaseConnection.DriverPath);
                con = DriverManager.getConnection(DataBaseConnection.DatabaseFile);
                String query = "select * from AdminStaff where ID = ?";
                ps = con.prepareStatement(query);
                  
                ps.setString(1,ID);
                rs = ps.executeQuery();
                
                DefaultTableModel model = (DefaultTableModel)StaffRecordTable.getModel();
                model.setRowCount(0); 
                
                while(rs.next()){
                    Object[] row = new Object[4];
                    row[0] = rs.getInt("ID");
                    row[1] = rs.getString("Name");
                    row[2] = rs.getString("Designation");
                    row[3] = rs.getString("Phone");
                    model.addRow(row); 
                }
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
        else{
            DefaultTableModel model = (DefaultTableModel)StaffRecordTable.getModel();
            model.setRowCount(0); 
            LoadAdminStaffRecordTable();
        }
    }//GEN-LAST:event_IDSearchButtonActionPerformed

