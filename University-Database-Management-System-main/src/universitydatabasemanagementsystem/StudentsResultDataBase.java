    private void IDSearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IDSearchButtonActionPerformed
        // TODO add your handling code here:
        if(!IDSearchField.getText().equals("")){
            Connection con = null;
            PreparedStatement ps = null;
            ResultSet rs = null;
            try{
                Class.forName(DataBaseConnection.DriverPath);
                con = DriverManager.getConnection(DataBaseConnection.DatabaseFile);
                
                String query = "select * from Results where Enrollment = ?";
                ps = con.prepareStatement(query);
                
                String Name = IDSearchField.getText();
                
                ps.setString(1,Name);
                rs = ps.executeQuery();
                
                DefaultTableModel model = (DefaultTableModel)ResultsRecordTable.getModel();
                model.setRowCount(0);
                
                while(rs.next()){
                    Object[] row = new Object[5];
                    row[0] = rs.getInt("Enrollment");
                    row[1] = rs.getString("Name");
                    row[2] = rs.getString("GPA");
                    row[3] = rs.getString("CGPA");
                    row[4] = rs.getString("CreditHours");
                    model.addRow(row);
                }
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
        else{
            DefaultTableModel model = (DefaultTableModel)ResultsRecordTable.getModel();
            model.setRowCount(0);
            LoadResultsRecordTable();
        }
    }//GEN-LAST:event_IDSearchButtonActionPerformed

