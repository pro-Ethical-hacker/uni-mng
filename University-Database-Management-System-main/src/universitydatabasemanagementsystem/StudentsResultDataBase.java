    /**
     * Loads student result records from the database into the ResultsRecordTable.
     * Populates the table with result records including Enrollment, Name, GPA, CGPA, and Credit Hours.
     */
    public void LoadResultsRecordTable(){
        Connection con = null;
        PreparedStatement ps = null;
        ResultSet rs = null;
        try{
            Class.forName(DataBaseConnection.DriverPath);
            con = DriverManager.getConnection(DataBaseConnection.DatabaseFile);
            String query = "select * from Results";
            ps = con.prepareStatement(query);
            rs = ps.executeQuery();
            DefaultTableModel model = (DefaultTableModel)ResultsRecordTable.getModel();
            while(rs.next()){
                Object[] row = new Object[5];
                    row[0] = rs.getString("Enrollment");
                    row[1] = rs.getString("Name");
                    row[2] = rs.getString("GPA");
                    row[3] = rs.getString("CGPA");
                    row[4] = rs.getString("CreditHours");
                model.addRow(row); 
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
